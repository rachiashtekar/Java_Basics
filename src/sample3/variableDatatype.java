package sample3;

public class variableDatatype {
public static void main(String[] args) {
	System.out.println("here we are learning about the variable and datatype");
	//integer
	int num=2;
	System.out.println("num :"+num);
	//string  here string is non primitive data type always start with uppercase 
	String name;   
	name="rachi";
	System.out.println("name :"+name);
	//character
	char grade;
	grade = 'A';
	System.out.println("grade :"+grade);
	//float
	float percentage=80.84f;
	System.out.println("percentage :"+percentage);
	//double
	double per=67.89d;
	System.out.println("per :"+per);
	
}
}
