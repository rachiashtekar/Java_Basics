package control_statement;

public class Switch {
	public static void main(String[] args) {
		switch(3) {
		case 1:
			System.out.println("you eneterd one here");
			break;
		case 2:
			System.out.println("you enterd two here");
			break;
		case 3:
			System.out.println("you enterd three here");
			break;
		default:
			System.out.println("you enterd wrong number");
			
		}
	}

}
